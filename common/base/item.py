class Item:
    def __init__(self, item_title, price, seller, image_url, store_url, location, rating):
        self.item_title = item_title
        self.price = price
        self.seller = seller
        self.image_url = image_url
        self.store_url = store_url
        self.location = location
        self.rating = rating
        self.shop_id = 0
